
# modulo is the operator %
# modulus is the remainder itself

number1 = int(input("First number: "))
number2 = int(input("Second number: "))
result1 = number1 / number2
modulo1 = number1 % number2
print(f"{number1} divided by {number2} equals {result1} with modulus {modulo1}")

# It returns a whole number integer of the remainder.
# If the number on the left is equal to the number on the right, it returns 0.
# If the number on the left is less than the number on the right, it returns the number on the left.
