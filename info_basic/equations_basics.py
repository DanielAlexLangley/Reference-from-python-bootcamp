
# EQUATIONS
# BASIC INFO

n = 0


n += 2      # use + to add
print(n)
# Output: 2


n -= 1      # use - to subtract
print(n)
# Output: 1


n *= 6      # use * to multiply
print(n)
# Output: 6


n /= 3      # use / to divide
print(n)    # division always gives float data type
# Output: 2.0


m = 3 ** 3  # use ** for exponents aka power, like 2 ** 3 which is 2x2x2 = 8
print(m)
# Output: 27
