
# DICTIONARIES
# BASIC INFO

# A dictionary has two parts to it.
# On the left is the key.
# On the right is the value.
# The format is:
# {Key: Value}

# TIPS:
# Put a comma after every item, even the last time, so it's ready for a new one to be added later on.
# Don't forget to put quotes around keys, or else it will think the keys are something other than strings...
# (unless you don't want strings, in which case enter it without quotes).

programming_dictionary = {
    "Bug": "An error in a program that prevents the program from running as expected.",
    "Function": "A piece of code that you can easily call over and over again.",
}

print(programming_dictionary)
# Output:
# {'Bug': 'An error in a program that prevents the program from running as expected.',
# 'Function': 'A piece of code that you can easily call over and over again.'}
